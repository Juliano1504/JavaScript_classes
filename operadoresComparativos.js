//Operadores de comparação

//Operador de igualdade ==
console.log(10==10);

//Operadro de estritamente igual ===
console.log(10==='10');

//Operador diferente de !=
console.log(10 != 5);

//Operador extritamente diferente de !==
console.log(10 !== '10');

//Operador maior que >
console.log(11 > 10);

//Operador menor que <
console.log(10 < 11);

//Operador maior ou igual >=
console.log(10 >= 10);

//Operador menor ou igual <=
console.log(10 <=10);

