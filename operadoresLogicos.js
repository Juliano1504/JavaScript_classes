//Operadores lógicos

// And ou e, representado por &&
/* 
true && true = true
true && false = false
false && false = false 
*/
console.log(1 == 1 && 2 == 2);

/* 
OR || 
true || true = true
false || true = true
false || false = false
*/
console.log(1==1 || 2==3);

//Operador de negação ! na frente da condição
console.log(!false && true);
